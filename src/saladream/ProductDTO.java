package saladream;

public class ProductDTO {
	private String ctgType;
	private String pno;
	private String pname;
	private int pprice;
	private int pstock;
	private String pinfo;
	private String pimage;
	private String pdescript;
	private String pdetailimage;
	
	public String getPdetailimage() {
		return pdetailimage;
	}
	public void setPdetailimage(String pdetailimage) {
		this.pdetailimage = pdetailimage;
	}
	public String getPdescript() {
		return pdescript;
	}
	public void setPdescript(String pdescript) {
		this.pdescript = pdescript;
	}
	public String getCtgType() {
		return ctgType;
	}
	public void setCtgType(String ctgType) {
		this.ctgType = ctgType;
	}
	public String getPno() {
		return pno;
	}
	public void setPno(String pno) {
		this.pno = pno;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPprice() {
		return pprice;
	}
	public void setPprice(int pprice) {
		this.pprice = pprice;
	}
	public int getPstock() {
		return pstock;
	}
	public void setPstock(int pstock) {
		this.pstock = pstock;
	}
	public String getPinfo() {
		return pinfo;
	}
	public void setPinfo(String pinfo) {
		this.pinfo = pinfo;
	}
	public String getPimage() {
		return pimage;
	}
	public void setPimage(String pimage) {
		this.pimage = pimage;
	}

}
